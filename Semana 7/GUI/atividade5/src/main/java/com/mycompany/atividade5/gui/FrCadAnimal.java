    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        Animal A1 = Campos_Para_Animal();
        
        if (editando == true) {
            animais.Atualiza_Animal(ID_Selecionado, A1);
            String Listar = animais.toString();
            this.edtListagem.setText("\n" + Listar);
            JOptionPane.showMessageDialog(this, "Animal editado com sucesso.");
        }else{
            if (!this.Verifica_Id(A1.getid())) {
                this.animais.AdicionaAnimal(A1);
                this.LimparCampos();
                this.Habilitar_Campos(false);
                String Listar = animais.toString();
                this.edtListagem.setText("\n" + Listar);
            }else{
                this.LimparCampos();
                this.Habilitar_Campos(false);
                JOptionPane.showMessageDialog(this, "O ID Já Existe.");
            }
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

