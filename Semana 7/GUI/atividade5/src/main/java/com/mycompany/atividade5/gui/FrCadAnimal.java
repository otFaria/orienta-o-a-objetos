    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       String ID_Escolhido = JOptionPane.showInputDialog("Me informe o ID para excluir.");
       Animal A1 = animais.ProcurarAnimal(ID_Escolhido);
       
        if (A1 != null) {
            int Index = animais.getAnimais().indexOf(A1);
            this.animais.getAnimais().remove(Index);
            String Listar = animais.toString();
            edtListagem.setText(Listar);
        }else{
            JOptionPane.showMessageDialog(this, "ID não encontrado.");
        }
       
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String ID_Escolhido = JOptionPane.showInputDialog("Informe o ID para ser editado: ", "");
        Animal A_Ser_Editado = this.animais.ProcurarAnimal(ID_Escolhido);
        
        if(A_Ser_Editado != null){
            this.editando = true;
            this.Objeto_Para_Campos(A_Ser_Editado);
            this.Habilitar_Campos_Edição(true);
            this.ID_Selecionado = A_Ser_Editado.getid();
        }else{
            JOptionPane.showMessageDialog(this, "O ID não existe.");
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed
    
    public boolean Verifica_Id(String ID){
        for (int i = 0; i < animais.getAnimais().size(); i++) {
            if(ID.equalsIgnoreCase(animais.getAnimais().get(i).getid())){
                return true;
            }       
        }
        
        return false;
    }
    
    public void Objeto_Para_Campos(Animal A1){
 
        edtID.setText(A1.getid());
        edtNome.setText(A1.getnome());
        edtEspécie.setText(A1.getespecie());
        edtIdade.setText(String.valueOf(A1.getidade()));
        edtPeso.setText(String.valueOf(A1.getpeso()));
        edtRaça.setText(A1.getraca());
    }
    
    public Animal Campos_Para_Animal(){
        Animal A1 = new Animal();
        A1.setid(edtID.getText());
        A1.setnome(edtNome.getText());
        A1.setespecie(edtEspécie.getText());
        A1.setidade(Integer.parseInt(edtIdade.getText()));
        A1.setpeso(Double.parseDouble(edtPeso.getText()));
        A1.setraca(edtRaça.getText());
        
        return A1;
    }
    
