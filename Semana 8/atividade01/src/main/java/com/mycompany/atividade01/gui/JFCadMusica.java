    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        this.Habilitar_Campos(false);
        this.Limpar_Campos();
        String ID_Escolhido = JOptionPane.showInputDialog("Me informe o ID da musica." , "");
        Musica musica_a_ser_editada = this.gerenciador.buscarMusica(Integer.parseInt(ID_Escolhido));
        
        if (musica_a_ser_editada != null){
            this.editando = true;
            this.Habilitar_Campos_Edicao(true);
            this.Preencher_Campos_Edt(musica_a_ser_editada);
            this.Index_Escolhido = gerenciador.getMusicas().indexOf(musica_a_ser_editada);
            JOptionPane.showMessageDialog(this, Index_Escolhido);
        }else{
            JOptionPane.showMessageDialog(this, "ID Invalido.");
            this.Limpar_Campos();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

