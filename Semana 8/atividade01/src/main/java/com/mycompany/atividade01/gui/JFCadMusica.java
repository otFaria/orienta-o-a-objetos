    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String ID_Musica = JOptionPane.showInputDialog("Informe o ID da Musica.");
        Musica M1 = gerenciador.buscarMusica(Integer.parseInt(ID_Musica));
        
        if (M1 != null){
            int index = gerenciador.getMusicas().indexOf(M1);
            this.gerenciador.getMusicas().remove(index);
            JOptionPane.showMessageDialog(this, "Musica deletada com sucesso.");
            edtListagem.setText(gerenciador.toString());
        }else{
            JOptionPane.showMessageDialog(this, "Musica não encontrada.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        if(editando){
          Musica M1 = Preencher_Campos_Musica();
          gerenciador.getMusicas().set(Index_Escolhido, M1);
          edtListagem.setText(gerenciador.toString());
          this.Limpar_Campos();
          this.Habilitar_Campos(false);
          JOptionPane.showMessageDialog(this, "Musica Atualizada com Sucesso");
          this.editando = false;
        }else{
            Musica M1 = Preencher_Campos_Musica();
            
            if(gerenciador.buscarMusica(M1.getID()) == null){
                gerenciador.adicionarMusica(M1);
                edtListagem.setText(gerenciador.toString());
                this.Habilitar_Campos(false);
                this.Limpar_Campos();
            }else{
                JOptionPane.showMessageDialog(this, "Já existe uma musica com este ID.");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

